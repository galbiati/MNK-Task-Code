function Boards(blackBoard,whiteBoard) {
	this.black_position = blackBoard;
	this.white_position = whiteBoard;
}

function Choices(choiceA,choiceB) {
	this.A = choiceA;
	this.B = choiceB;
}

function State(blackBoard, whiteBoard, choiceA, choiceB, color) {
	this.boards = new Boards(blackBoard, whiteBoard);
	this.choices = new Choices(choiceA, choiceB);
	this.color = color;
    tempB = this.boards.black_position.split("");
    tempW = this.boards.white_position.split("");
    if(color == "black") {
        tempB[this.choices.A] = "0";
        tempB[this.choices.B] = "0";
        this.boards.black_position = tempB.join("");
    } else {
        tempW[this.choices.A] = "0";
        tempW[this.choices.B] = "0";
        this.boards.white_position = tempW.join("");
    }
}

